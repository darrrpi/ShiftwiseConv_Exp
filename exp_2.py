# 4. ЗАГЛУШКА SHIFTADD
class ShiftAddConv(nn.Module):
    def __init__(self, in_channels, out_channels, kernel_size=3,
                 stride=1, padding=1, groups=1, bias=False,
                 sparse_gap=1, num_paths=4, shift_mode='diagonal', **kwargs):
        super().__init__()

        self.sparse_gap = max(1, sparse_gap)
        pad = kernel_size // 2 if isinstance(kernel_size, int) else kernel_size[0] // 2

        self.main_conv = nn.Conv2d(in_channels, out_channels, kernel_size,
                                   stride=stride, padding=pad, groups=groups, bias=bias)

        gap = self.sparse_gap
        if shift_mode == 'diagonal':
            self.shifts = [(gap, gap), (-gap, -gap), (gap, -gap), (-gap, gap)]
        elif shift_mode == 'cross':
            self.shifts = [(gap, 0), (-gap, 0), (0, gap), (0, -gap)]
        else:
            self.shifts = [(gap, gap), (-gap, -gap), (gap, -gap), (-gap, gap),
                          (gap, 0), (-gap, 0), (0, gap), (0, -gap)]
        self.shifts = self.shifts[:num_paths]

        self.shift_convs = nn.ModuleList([
            nn.Conv2d(in_channels, out_channels, kernel_size,
                      stride=stride, padding=pad, groups=groups, bias=False)
            for _ in self.shifts
        ])

        nn.init.kaiming_normal_(self.main_conv.weight, mode='fan_out')
        for sc in self.shift_convs:
            nn.init.kaiming_normal_(sc.weight, mode='fan_out')
            sc.weight.data.mul_(0.1)

    def forward(self, x):
        out = self.main_conv(x)
        for i, (sh, sw) in enumerate(self.shifts):
            shifted = torch.roll(x, shifts=(sh, sw), dims=(2, 3))
            out = out + self.shift_convs[i](shifted) * 0.3
        return out

