class LayerNorm2d(nn.Module):
    def __init__(self, dim, eps=1e-6):
        super().__init__()
        self.norm = nn.LayerNorm(dim, eps=eps)
    def forward(self, x):
        return self.norm(x.permute(0, 2, 3, 1)).permute(0, 3, 1, 2)

class ShiftWiseBlock(nn.Module):
    def __init__(self, dim, sparse_gap=3, num_paths=4, shift_mode='diagonal', expand=4):
        super().__init__()
        self.dw = ShiftAddConv(dim, dim, 3, padding=1, groups=dim, bias=False,
                               sparse_gap=sparse_gap, num_paths=num_paths, shift_mode=shift_mode)
        self.norm = LayerNorm2d(dim)
        self.pw1 = nn.Conv2d(dim, dim * expand, 1)
        self.act = nn.GELU()
        self.pw2 = nn.Conv2d(dim * expand, dim, 1)
        self.gamma = nn.Parameter(1e-6 * torch.ones(dim, 1, 1))
    def forward(self, x):
        return x + self.gamma * self.pw2(self.act(self.pw1(self.norm(self.dw(x)))))

class ShiftWiseCIFAR(nn.Module):
    DIMS = [64, 128, 256, 512]
    DEPTHS = [2, 2, 6, 2]

    def __init__(self, num_classes=100, sparse_gap=3, num_paths=4, shift_mode='diagonal'):
        super().__init__()
        self.stem = nn.Sequential(
            nn.Conv2d(3, self.DIMS[0], 3, padding=1, bias=False),
            LayerNorm2d(self.DIMS[0]),
        )
        self.stages = nn.ModuleList()
        for i, (dim, depth) in enumerate(zip(self.DIMS, self.DEPTHS)):
            layers = []
            if i > 0:
                layers += [nn.Conv2d(self.DIMS[i-1], dim, 2, stride=2, bias=False), LayerNorm2d(dim)]
            for _ in range(depth):
                layers += [ShiftWiseBlock(dim, sparse_gap, num_paths, shift_mode)]
            self.stages.append(nn.Sequential(*layers))
        self.norm = LayerNorm2d(self.DIMS[-1])
        self.head = nn.Sequential(nn.AdaptiveAvgPool2d(1), nn.Flatten(), nn.Linear(self.DIMS[-1], num_classes))

    def forward(self, x):
        x = self.stem(x)
        for stage in self.stages:
            x = stage(x)
        return self.head(self.norm(x))

